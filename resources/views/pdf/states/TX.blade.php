        Many serious illnesses such as cancer, failure of major organs
        (kidney, heart, liver or lung), and serious brain disease such as
        Alzheimer’s dementia may be considered irreversible early on. There
        is no cure, but the patient may be kept alive for prolonged periods
        of time if the patient receives life-sustaining treatments. Late in
        the course of the same illness, the disease may be considered
        terminal when, even with treatment, the patient is expected to die.
        You may wish to consider which burdens of treatment you would be
        willing to accept in an effort to achieve a particular outcome. This
        is a very personal decision that you may wish to discuss with your
        physician, family, or other important persons in your life.</span></p>
