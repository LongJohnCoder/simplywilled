        <p  style="margin-bottom: 0.17in; line-height: 100%"><span style="font-family:'Times New Roman, serif'">Except
        to the extent you state otherwise, this document gives the person you
        name as your agent the authority to make any and all health care
        decisions for you in accordance with your wishes, including your
        religious and moral beliefs, when you are no longer capable of making
        them yourself. Because “health care” means any treatment,
        service, or procedure to maintain, diagnose, or treat your physical
        or mental condition, your agent has the power to make a broad range
        of health care decisions for you. Your agent may consent, refuse to
        consent, or withdraw consent to medical treatment and may make
        decisions about withdrawing or withholding life-sustaining treatment.
        Your agent may not consent to voluntary inpatient mental health
        services, convulsive treatment, psychosurgery, or abortion. A
        physician must comply with your agent’s instructions or allow you
        to be transferred to another physician.</span></p>
        <p  style="margin-bottom: 0.17in; line-height: 100%"><span style="font-family:'Times New Roman, serif'">Your
        agent’s authority begins when your doctor certifies that you lack
        the competence to make health care decisions.</span></p>
        <p  style="margin-bottom: 0.17in; line-height: 100%"><span style="font-family:'Times New Roman, serif'">Your
        agent is obligated to follow your instructions when making decisions
        on your behalf. Unless you state otherwise, your agent has the same
        authority to make decisions about your health care as you would have
        had.</span></p>
        <p  style="margin-bottom: 0.17in; line-height: 100%"><span style="font-family:'Times New Roman, serif'">It
        is important that you discuss this document with your physician or
        other health care provider before you sign it to make sure that you
        understand the nature and range of decisions that may be made on your
        behalf. If you do not have a physician, you should talk with someone
        else who is knowledgeable about these issues and can answer your
        questions. You do not need a lawyer’s assistance to complete this
        document, but if there is anything in this document that you do not
        understand, you should ask a lawyer to explain it to you.</span></p>
        <p  style="margin-bottom: 0.17in; line-height: 100%"><span style="font-family:'Times New Roman, serif'">The
        person you appoint as agent should be someone you know and trust. The
        person must be 18 years of age or older or a person under 18 years of
        age who has had the disabilities of minority removed. If you appoint
        your health or residential care provider (e.g., your physician or an
        employee of a home health agency, hospital, nursing home, or
        residential care home, other than a relative), that person has to
        choose between acting as your agent or as your health or residential
        care provider; the law does not permit a person to do both at the
        same time.</span></p>
        <p  style="margin-bottom: 0.17in; line-height: 100%"><span style="font-family:'Times New Roman, serif'">You
        should inform the person you appoint that you want the person to be
        your health care agent. You should discuss this document with your
        agent and your physician and give each a signed copy. You should
        indicate on the document itself the people and institutions who have
        signed copies. Your agent is not liable for health care decisions
        made in good faith on your behalf.&nbsp;</span></p>
